# Taking Correct Input Using Type Casting
#Type checking means finding the data type of a value.
age = int(input("Enter your age: "))  # Taking input from user and converting it to integer
print(type(age))  # Displaying the type of age variable
n=str(age)
print(n)  # Displaying the value of age variable as string
print((type(n)))  # Displaying the type of age variable as string 
p=float(age)
print(p)  # Displaying the value of age variable as float
print((type(p)))  # Displaying the type of age variable as float
q=bool(age)
print(q)  # Displaying the value of age variable as boolean
print((type(q)))  # Displaying the type of age variable as boolean
r=complex(age)
print(r)  # Displaying the value of age variable as complex
print((type(r)))  # Displaying the type of age variable as complex 

